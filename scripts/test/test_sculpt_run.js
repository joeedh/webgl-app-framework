window._testSculpt = function(tool=SculptTools.CLAY, args={}, toolop="bvh.paint") {
  let ps = sculpt_ps;
  let ps2 = [];

  for (let p of ps) {
    let p2 = {};

    p2.viewvec = new Vector3(p.viewvec);
    p2.viewp = new Vector3(p.viewp);
    p2.mpos = new Vector2(p.mpos);
    p2.rendermat = new Matrix4(p.rendermat);

    ps2.push(p2);
  }
  ps = ps2;

  let divs = args.divs !== undefined ? args.divs : 85;
  let toSphere = args.toSphere !== undefined ? args.toSphere : 1.0;

  console.log(ps);

  let test = new TestSet();

  test.pushAppState();
  test.func(() => {
    let sarea = _appstate.ctx.propsbar.owning_sarea;
    _appstate.screen.collapseArea(sarea);
  });

  test.pathSet("scene.toolmode[object]", true);
  test.redraw();

  test.tool("object.toggle_select_all(mode='ADD')");
  test.tool("object.delete_selected()");
  test.tool("mesh.procedural_add()");

  console.log("DIVS", divs, "toSphere", toSphere);
  test.redraw();

  test.pathSet("object.data.generator.dimen", divs);
  test.pathSet("object.data.generator.toSphere", toSphere);
  test.redraw();

  test.tool("mesh.procedural_to_mesh()");
  test.redraw();

  let dosort = args.sort === undefined ? true : args.sort;

  test.func(() => {
    let ctx = _appstate.ctx;
    let mesh = ctx.mesh;

    window.updateDataGraph(true);
    let bvh = mesh.getBVH();

    if (dosort) {
      console.error("Spatially sorting mesh topology into patches");
      bvh.spatiallySortMesh(mesh);
    }
  });

  test.wait(500);

  test.pathSet("scene.toolmode[sculpt]", true);
  test.redraw();

  test.wait(350);

  if (1) {
    test.perfStart("sculpt");

    toolop = test.sculpt(tool, args, toolop);
    for (let p of ps) {
      toolop.append(p.viewp, p.viewvec, p.rendermat, p.mpos);
    }

    test.perfLog("sculpt");
  }

  test.redraw();
  test.wait(500);
  test.popAppState();
  test.redraw();

  let logAccum = {};
  let tot = 0;
  let max = 15;

  let preroll = 2;

  window._test = test;

  test.start = function() {
    if (window._timer !== undefined) {
      console.log("Stopping timer");
      window.clearInterval(window._timer);
    }

    console.log("Starting timer");
    window._timer = window.setInterval(() => {
      if (test.running) {
        return;
      }

      test.reset();
      test.run().then(() => {
        tot++;

        if (tot >= preroll) {
          for (let k in test.lastLogDeltas) {
            if (!(k in logAccum)) {
              logAccum[k] = 0.0;
            }

            logAccum[k] += test.lastLogDeltas[k];
            let avg = logAccum[k] / (tot - preroll + 1);

            console.error(`avg: ${k}: ${avg.toFixed(2)}ms`);
            test.showMessage(`avg: ${k}: ${avg.toFixed(2)}ms`);
          }
        }

        if (tot >= max) {
          test.stop();
        }
      });
    }, 500);
  }

  test.stop = function() {
    if (window._timer !== undefined) {
      console.warn("Stopping timer");

      window.clearInterval(window._timer);
      window._timer = undefined;
    }
  }

  return test;
}

